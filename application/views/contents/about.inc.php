      <h3>About Microbe</h3>
      <p>
        Microbe PHP Framework is lightweight PHP framework for simple site construction.
      </p>
      <p>
        One of the goals of this project is Router-View architecture.
        The application render a layout directly after routing.
        You must define routing rules and create corresponding views.
        Controllers unnecessary at all.
        This approach applicable for simple sites without database connection and landing pages.
      </p>
      <p>
        Project under developement. Please report about bugs.
      </p>
      <h4>
        Short help
      </h4>
      <ul>
        <li>Unzip or git clone package in root application directory.
        <li>Site documents' root must be <span class="app-span-highlight">'./web/'</span> directory.
        <li>Correct <span class="app-span-highlight">'./web/.htaccess'</span>
            and <span class="app-span-highlight">'./web/index.php'</span>  (optional).
        <li>Edit config in
            <span class="app-span-highlight">'./application/configs/config.txt'</span>.
        <li>Edit routes in 
            <span class="app-span-highlight">'./application/configs/routes.json'</span>.
        <li>Edit variables in
            <span class="app-span-highlight">'./application/configs/vars.json'</span>
            (optional).
        <li>Add data models in
            <span class="app-span-highlight">'./application/Models/'</span>
            (optional).
        <li>Add controllers and edit action handlers in
            <span class="app-span-highlight">'./application/Controllers/'</span>
            (optional).
        <li>Add classes to
            <span class="app-span-highlight">'./application/Classes/'</span>
            (optional).
        <li>Place Your layouts, templates, blocks in
            <span class="app-span-highlight">'./application/views/'</span> directory.
        <li>Enjoy.
      </ul>

<!-- } Content -->
